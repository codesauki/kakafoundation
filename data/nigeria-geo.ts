export function getStateNames(): string[] {
  return NIGERIA_STATES.map((s) => s.name).sort();
}

export function getLGAsForState(stateName: string): LGAData[] {
  const state = NIGERIA_STATES.find((s) => s.name === stateName);
  return state ? state.lgas : [];
}

export function getLGANames(stateName: string): string[] {
  return getLGAsForState(stateName).map((l) => l.name);
}

export function getWardsForLGA(stateName: string, lgaName: string): string[] {
  const lgas = getLGAsForState(stateName);
  const lga = lgas.find((l) => l.name === lgaName);
  return lga ? lga.wards : [];
}
